DROP TABLE &1.ACCOUNT;

CREATE TABLE &1.ACCOUNT(
    ACCOUNT_NO          VARCHAR(20)      CONSTRAINT PK_ACCOUNT PRIMARY KEY,
    MEMBER_ID           VARCHAR2(20)    NOT NULL,
    PASSWORD            VARCHAR2(4),
    AMOUNT              NUMBER(15)      DEFAULT 0,
    GOODS_CL            NUMBER(3),
    GOODS_CODE          NUMBER(3),
    AVAILABLE            CHAR(1)         DEFAULT 'Y'
        CONSTRAINT CK_ACCOUNT_AVAILABLE CHECK (AVAILABLE IN('Y', 'N')),
    REG_DATE            DATE            DEFAULT SYSDATE,
    CONSTRAINT FK_ACCOUNT_MEMBER_MEMBER_ID FOREIGN KEY (MEMBER_ID)
        REFERENCES &1.MEMBER(ID)
);


